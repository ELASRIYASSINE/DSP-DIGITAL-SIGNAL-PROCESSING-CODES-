	.text
A	.int 1,2,3,4,5,6,7,8,9	;MATRICE A DE 3x ENTIERS 
B	.int 1,2,3		;MATRICE A DE 1x ENTIERS
tab1	.word A			;TABLEAU 1 QUI CONTIENT LA MATRICE A
tab2	.word B			;TABLEAU 2 QUI CONTIENT LA MATRICE B
resul	.word 24h		;RESULTAT DES MATRICES STOCKER A PARTIR DE L'ADRESSE 50h
	ldi @tab1,AR0		;CHARGEMENT DU TABLEAU 1 DANS AR0      
	ldi @tab2,AR1		;CHARGEMENT DU TABLEAU 2 DANS AR1
	ldi @resul,AR2		;CHARGEMENT DU RESULTAT DANS AR2
	ldi 3,R4 		;CHARGEMENT DE NOMBRE DE DONNEE DANS LA COLONNE A DANS R4
loopi	ldi 0,R0		;REMISE A ZERO DE R0
	ldi 3,R5 		;CHARGEMENT DE NOMBRE DE DONNEE DANS LA COLONNE B DANS R5
loopj	mpyi *AR0++,*AR1++,R1	;MULTIPLICATION DES DONNEES DE AR0 PAR AR1 ET SOCKAGE SUR R1
	addi R1,R0		;AJOUTER R1 SUR R0 ET STOCKER SUR RO
	subi 1,R5		;SOUSTRAIRE 1 DU R5 (R5=3-1=2)
	bnz loopj		;REBRANCHER SUR LOOPJ SI L'ACCUMULATEUR EST NON EGALE A ZERO.
	sti R0,*AR2++		;STOCKAGE DE R0 SUR LE PIONTEUR AR2
	ldi @tab2,AR1		;CHARGER LE TABLEAU 2 SUR AR1
	subi 1,R4		;SOUSTRAIRE R4 DE 1 (R4=3-1=2)
	bnz loopi		;REBRANCHER SUR LOOPI SI L'ACCUMULATEUR EST NON EGALE A ZERO.
	.end
	